				<h2>How to Achieve Queen Bee Status</h2>
				<p>
					To reach Queen Bee, you must find every valid word that the puzzle creators have included in that day's puzzle. The word list is pre-determined by the NYT, so you're trying to match their complete list.
				</p>

				<h2>What Words Count?</h2>
				<p>Not all words you might think of will be accepted. The New York Times excludes:</p>
				<ul>
					<li>Proper nouns (names, places)</li>
					<li>Offensive or obscure words</li>
					<li>Hyphenated words</li>
					<li>Words that are always capitalized</li>
					<li>Abbreviations, prefixes, and suffixes</li>
				</ul>

