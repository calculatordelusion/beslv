				<h2>How Levels Work</h2>
				<p>
					Your rank is determined by calculating your score as a percentage of the maximum possible points. For example:
				</p>
				<ul>
					<li>If the total possible points is 200 and you score 100 points, you've reached 50% (Amazing level)</li>
					<li>If you score 140 points out of 200, you've reached 70% (Genius level)</li>
					<li>To reach Queen Bee, you must score 100% by finding every word</li>
				</ul>

