				<h2>About Daylight Saving Time</h2>
				<p>
					Eastern Time (ET) follows Daylight Saving Time, which means:
				</p>
				<ul>
					<li><strong>Spring through Fall:</strong> Eastern Daylight Time (EDT) = UTC-4</li>
					<li><strong>Fall through Spring:</strong> Eastern Standard Time (EST) = UTC-5</li>
				</ul>
				<p>
					This can cause the release time in your timezone to shift by one hour depending on the time of year.
				</p>

